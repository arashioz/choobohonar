#!/usr/bin/env node
import { cpSync, existsSync, mkdirSync, readdirSync, rmSync, statSync, writeFileSync } from "node:fs";
import { basename, dirname, extname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "../..");
const publicRoot = resolve(__dirname, "../public/images/projects");

const projects = [
  { slug: "aknoon-residence", source: join(root, "05  Aknoon") },
  { slug: "shenaj-villa", source: join(root, "02  Shenaj project") },
  { slug: "armon-hotel", source: join(root, "03  Hotel Armon") },
  { slug: "araz-suite", source: join(root, "04  Hotel Araz") },
];

const imageExt = new Set([".jpg", ".jpeg", ".png", ".webp", ".JPG", ".JPEG", ".PNG", ".WEBP"]);

function walkImages(dir) {
  const files = [];
  for (const entry of readdirSync(dir)) {
    const full = join(dir, entry);
    const stat = statSync(full);
    if (stat.isDirectory()) {
      files.push(...walkImages(full));
      continue;
    }
    if (imageExt.has(extname(entry))) files.push(full);
  }
  return files.sort((a, b) => a.localeCompare(b, "en"));
}

function normalizeExt(filePath) {
  const ext = extname(filePath).toLowerCase();
  if (ext === ".jpeg") return ".jpg";
  return ext === ".jpg" || ext === ".png" || ext === ".webp" ? ext : ".jpg";
}

const manifest = {};

for (const project of projects) {
  if (!existsSync(project.source)) {
    console.warn(`Skip missing source: ${project.source}`);
    continue;
  }

  const destDir = join(publicRoot, project.slug);
  if (existsSync(destDir)) rmSync(destDir, { recursive: true, force: true });
  mkdirSync(destDir, { recursive: true });

  const images = walkImages(project.source);
  const paths = [];

  images.forEach((src, index) => {
    const name = `${String(index + 1).padStart(2, "0")}${normalizeExt(src)}`;
    const dest = join(destDir, name);
    cpSync(src, dest);
    paths.push(`/images/projects/${project.slug}/${name}`);
  });

  manifest[project.slug] = paths;
  console.log(`${project.slug}: ${paths.length} images`);
}

const outFile = resolve(__dirname, "../src/data/project-image-manifest.ts");
const body = `// Auto-generated by scripts/sync-project-images.mjs — do not edit manually.\n\nexport const projectImageManifest = ${JSON.stringify(manifest, null, 2)} as const;\n\nexport type ProjectSlug = keyof typeof projectImageManifest;\n\nexport function getProjectImages(slug: ProjectSlug): readonly string[] {\n  return projectImageManifest[slug] ?? [];\n}\n`;

writeFileSync(outFile, body, "utf8");
console.log(`Wrote ${outFile}`);
